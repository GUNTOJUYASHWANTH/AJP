//program on relational Operator
public class RelationalOperatorEx{
	public static void main(String[] args){
		int num1=12;
		int num2=23;
	
		
		System.out.println(num1==num2);
		
		System.out.println(num1>num2);
	
		System.out.println(num1<num2);
		

		System.out.println(num1>=num2);
		
		System.out.println(num1<=num2);
	
		System.out.println(num1!=num2);
}
}
	
	