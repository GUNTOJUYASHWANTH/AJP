//LOGICAL OPERATOR
public class LogicalOperatorEx{
	public static void main(String[] args){
		int num1=12;
		int num2=23;
	
		
		System.out.println(num1==num2);
		
		System.out.println(num1>num2);
	
		System.out.println(num1<num2);
		

		System.out.println(num1>=num2);
		
		System.out.println(num1<=num2);
	
		System.out.println(num1!=num2);
		System.out.println("LOGICAL OPERATOR");

		boolean a=true;
		boolean b=false;

		System.out.println(a&&b);
		System.out.println(a||b);
		System.out.println(!a);
		System.out.println(!b);

}
}
